    /**
     * Checks validity of id
     *
     * @param id
     *
     * @return Boolean value showing if id is valid or not
     */
    public static boolean isIdValid(String id) {
        Pattern pattern = Pattern.compile("\\d+");
        if (id == null) {
            return false;
        }
        return pattern.matcher(id).matches();
    }

